package sortArray;

public class User {

	 private String department;
	    private double salary;

	    public User(String department, double salary) {
	        this.department = department;
	        this.salary = salary;
	    }

	    public String getDepartment() {
	        return department;
	    }

	    public double getSalary() {
	        return salary;
	    }
}
